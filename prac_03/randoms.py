import random

print(random.randint(5, 20))  # line 1
# Output could be any integer between 5 and 20
# Smallest number: 5
# Largest number: 20
print(random.randrange(3, 10, 2))  # line 2
# Output could be 3, 5, 7, or 9
# Smallest number: 3
# Largest number: 9
print(random.uniform(2.5, 5.5))  # line 3
# Output could be any floating-point number between 2.5 and 5.5
# Smallest number: 2.5
# Largest number: 5.5
print(random.randint(1, 100))